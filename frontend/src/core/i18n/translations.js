export const useI18n = create((set, get) => ({
  lang: localStorage.getItem("ambyoai.lang") || "en",
  setLang: (lang) => {
    localStorage.setItem("ambyoai.lang", lang);
    set({ lang });
  },
  t: (key, vars) => {
    const l = get().lang;
    let s = (translations[l] && translations[l][key]) || translations.en[key] || key;
    if (vars) Object.keys(vars).forEach((k) => { s = s.replace(`{${k}}`, vars[k]); });
    return s;
  },
}));
